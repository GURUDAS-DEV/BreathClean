def _normalize_host(value: str) -> str:
    host = (value or '').strip()
    if not host:
        return ''
    if '://' in host:
        host = (urlparse(host).hostname or '').strip()
    else:
        host = host.split('/')[0].split(':')[0].strip()
    return host.lower()


